/**
 * Polices pixel à largeur variable, identiques à la préview web.
 * `' '` est un séparateur de groupe (2 px de blanc), géré par le renderer.
 * `'` (apostrophe) = symbole prime, notation des minutes.
 */
class Font(val height: Int, val glyphs: Map<Char, Array<String>>) {
    fun charWidth(c: Char): Int = glyphs[c]?.get(0)?.length ?: 0

    fun textWidth(s: String): Int {
        var w = 0
        for (c in s) w += if (c == ' ') 1 else charWidth(c) + 1
        return w - 1
    }
}

